            metodos mt = new metodos();
            String SQL = "Select @i := @i + 1 as 'NÂ°', cantidad AS Cantidad, producto.nombre AS Descripcion, venta_iva AS 'Venta Iva', ROUND((cantidad * venta_iva),2) AS Total FROM producto, proveedores, (select @i := 0) as id WHERE producto.id_proveedor = proveedores.id_proveedor AND id_requerimiento = " + id_req;
            tbconsumidorfinal.setModel(mt.Consulta(SQL));
            rs.next();
            Double suma = rs.getDouble("total");
            String sum = String.valueOf(suma);
            txttotalpagar.setText(sum);
