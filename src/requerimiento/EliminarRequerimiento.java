    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int filasel;
        filasel = tbrequerimientos.getSelectedRow();
        if (filasel == -1) {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ningun registro");
        } else {
        int p = JOptionPane.showConfirmDialog(null, "Â¿Desea Eliminar el registro?", "Borrar Registro", JOptionPane.YES_NO_OPTION);
        if (p == 0) {
            String id = tbrequerimientos.getValueAt(tbrequerimientos.getSelectedRow(), 0).toString();
            mt.Insertar("Delete from requerimiento where id_requerimiento =" + id);
            tbrequerimientos.setModel(mt.Consulta("select id_requerimiento, numero, cliente, fecha from requerimiento ORDER BY id_requerimiento"));
            RequerimientoForm.comboreq("SELECT id_requerimiento, numero FROM requerimiento ORDER BY id_requerimiento");
            RequerimientoForm.Cargar("");
            RequerimientoForm.showclient();
            RequerimientoForm.showreq();
        }
        }

    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed

        if (tbrequerimientos.isEditing()){
            TableCellEditor tce = tbrequerimientos.getCellEditor();
            tce.stopCellEditing();
        }
        try {
            int filasel = tbrequerimientos.getSelectedRow();

            if (filasel == -1) {
                JOptionPane.showMessageDialog(null, "No se ha seleccionado ningun Registro");
            } else {
                String id = tbrequerimientos.getValueAt(tbrequerimientos.getSelectedRow(), 0).toString();

                String sql = ("Update requerimiento set numero='" + tbrequerimientos.getValueAt(tbrequerimientos.getSelectedRow(), 1) + "', cliente='" + tbrequerimientos.getValueAt(tbrequerimientos.getSelectedRow(), 2) + "', fecha='" + tbrequerimientos.getValueAt(tbrequerimientos.getSelectedRow(), 3) + "' where id_requerimiento=" + id);
                mt.Insertar(sql);
                tbrequerimientos.setModel(mt.Consulta("select id_requerimiento, numero, cliente, fecha from requerimiento ORDER BY id_requerimiento"));
                RequerimientoForm.comboreq("SELECT id_requerimiento, numero FROM requerimiento ORDER BY id_requerimiento");
                RequerimientoForm.Cargar("");
                RequerimientoForm.showclient();
                RequerimientoForm.showreq();
                
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }//GEN-LAST:event_btnmodificarActionPerformed

